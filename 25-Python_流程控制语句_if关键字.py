# if关键字的语句结构
# if判断条件：
#     代码(判断条件问True时，才会执行if下面的内容)

age=12
# 若age大于18，就可以开车了
if age>18:
    print('你可以开车了')

# True代表男性，False代表女性
gender=True

if gender==True:
    # if下面的代码，必须是一个tab键，或是四个空格
    print('你是个男的')
