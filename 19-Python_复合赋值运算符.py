a=1
# += 加法赋值运算符
# a=a+2
a += 2   #等于a=a+2
print(a)    #3

b=2
# -= 减法赋值运算符
# b=b-3
b -= 3
print(b)    #-1

c=2
# *= 乘法赋值运算符
# c=c*0
c *= 0
print(c)    #0

# /= 除法赋值运算符
d=5
d=d/4
# d /= 4
print(d)    #1.25

# //= 取整除赋值运算符
e=7
# e=e//3
e //= 3
print(e)    #2

# %= 取模赋值运算符
f=8
# f=f%5
f %= 5
print(f)    #3

# **= 幂赋值运算符
g=2
# f=f**3
g **=3
print(g)    #8


