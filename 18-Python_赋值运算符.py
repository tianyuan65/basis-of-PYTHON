# 为单个变量赋值
a=8
print(a)

# 同时为多个变量赋值，按下面的例子，将数据23先赋值给变量c，再将以获取数据的变量c赋值给变量b。
b=c=23
print(b)    #23
print(c)    #23

# 多个变量赋值，用逗号进行分隔，同时给多个变量赋值不同的数据。
d,e,f=67,'身着长衫轻摇纸扇',False
print(d)    #67
print(e)    #身着长衫轻摇纸扇
print(f)    #False
